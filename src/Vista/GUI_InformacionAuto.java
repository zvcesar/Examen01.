    public String generarRegistro(boolean agregar, int registro)
    {
     if(agregar)
     {
      registro++;   
     }
     else 
     {
         registro=1;
     }
     
     return ""+registro;
        
    }
    public void colocarRegistro(boolean agregar, int registro)
    {
     jt_NumeroRegistro.setText(generarRegistro(agregar, registro));
    }
