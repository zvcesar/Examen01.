    public String[] devolverInformacion()
    {
        String arreglo[];
        arreglo=new String[4];
        arreglo[0]=this.jt_NumeroRegistro.getText();
        arreglo[1]=this.jt_CedulaDueño.getText(); 
        arreglo[2]=this.jt_NombreDueño.getText();
        arreglo[3]=this.jt_PlacaAuto.getText();
        
        return arreglo;
    }
